#include "../../includes/minirt.h"

t_matrix	matrix_get_scale(t_vector scale)
{
	t_matrix	m;

	m = matrix_alloc(4);
	if (!m.m)
		return (m);
	m.m[0][0] = scale.x;
	m.m[0][1] = 0;
	m.m[0][2] = 0;
	m.m[0][3] = 0;
	m.m[1][0] = 0;
	m.m[1][1] = scale.y;
	m.m[1][2] = 0;
	m.m[1][3] = 0;
	m.m[2][0] = 0;
	m.m[2][1] = 0;
	m.m[2][2] = scale.z;
	m.m[2][3] = 0;
	m.m[3][0] = 0;
	m.m[3][1] = 0;
	m.m[3][2] = 0;
	m.m[3][3] = 1;
	return (m);
}
